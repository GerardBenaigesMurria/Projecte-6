# Descripció: Et diu si el teu numero es més gran o més petit que 0 i també si es 0
# Especificaicons de entrada: Et demana un numero enter 

num = int(input("Fica un numero "))

if num > 0:
    print("El número és més gran que 0")

if num < 0:
    print("El número és més petit que 0")

if num == 0:
    print("El número és 0")