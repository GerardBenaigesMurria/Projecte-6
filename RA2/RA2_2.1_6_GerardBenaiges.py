# Descripció: Et diu si la teva lletra es vocal o consonant
# Especificaicons de entrada: Ficar una lletra del abecedari en minuscula

lletra = str(input("Fica una lletra del abecedari "))

if lletra == "a" or lletra == "e" or lletra == "i" or lletra == "u" or lletra == "o":
    print("La teva lletra es una vocal")

else:
    print("La teva lletra es una consonant")