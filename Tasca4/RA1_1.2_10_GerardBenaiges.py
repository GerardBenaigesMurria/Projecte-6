num1 = int(input("Fica el primer numero "))
num2 = int(input("Fica el segon numero "))

operació = input("Fica un d'aquest simbols(+, -, *, /) ")

if(operació == "+"):
    print(num1 + num2)

if(operació == "-"):
    print(num1 - num2)

if(operació == "*"):
    print(num1 * num2)

if(operació == "/"):
    print(num1 / num2)